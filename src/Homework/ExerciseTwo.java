package Homework;

/*
Write a Java programme using the following steps.
2.1 Declare two static variables
2.2 Declare one static method
2.3 Call both static variables into the static method inside the print
statement. 2.4 Declare the Main method.
2.5 Call the static method into the Main method and Run the programme.
*/

public class ExerciseTwo {

    static int a = 10; //Static int
    static String name = "Kirtan"; //Static String

    public static void main(String[] args) { //Declare main method
        System.out.println(a); //call static int
        System.out.println(name); //call static string

    }
}
